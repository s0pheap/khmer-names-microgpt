"""
Reads both CSV sources, expands each name into 3 styles (given, surname, full)
for both Khmer and Latin, deduplicates, and writes the training file.

Sources:
  data/khmer_names.csv  — primary validated dataset
  data/extra_names.csv  — generated by khmer_name_pool.py

Output:
  data/khmer_names_training.txt — alternating Khmer + Latin lines, deduplicated

Run: python utils/build_dataset.py
"""
import csv
from pathlib import Path

DATA_DIR = Path(__file__).parent.parent / "data"


def read_csv(path: Path) -> list[tuple[str, str]]:
    """Read a names CSV and return list of (khmer_full, latin_full) pairs."""
    with open(path, newline="", encoding="utf-8") as f:
        return [
            (
                f"{row['surname'].strip()} {row['givenName'].strip()}",
                f"{row['romanizedSurname'].strip().lower()} {row['romanizedGiven'].strip().lower()}",
            )
            for row in csv.DictReader(f)
        ]


def three_styles(name: str) -> list[str]:
    """Expand 'surname given' into [given, surname, full]."""
    parts = name.split()
    return [" ".join(parts[1:]), parts[0], name] if len(parts) >= 2 else [name]


if __name__ == "__main__":
    # ── 1. Load both CSVs ─────────────────────────────────────────────────────
    all_pairs: list[tuple[str, str]] = []

    for csv_file in ["khmer_names.csv", "extra_names.csv"]:
        path = DATA_DIR / csv_file
        if path.exists():
            pairs = read_csv(path)
            all_pairs.extend(pairs)
            print(f"{csv_file}: {len(pairs)} pairs loaded")
        else:
            print(f"[info] {csv_file} not found, skipping")

    print(f"Total pairs: {len(all_pairs)}")

    # ── 2. Expand each full name into 3 styles ────────────────────────────────
    # e.g. "សេង វិជ្ជា" / "seng vichhea"
    # becomes:
    #   ("វិជ្ជា",     "vichhea")       ← given name only
    #   ("សេង",        "seng")          ← surname only
    #   ("សេង វិជ្ជា", "seng vichhea")  ← full name
    expanded: list[tuple[str, str]] = []
    for kh, lat in all_pairs:
        for kh_style, lat_style in zip(three_styles(kh), three_styles(lat)):
            expanded.append((kh_style, lat_style))

    # ── 3. Deduplicate — keep first occurrence of each (kh, lat) pair ─────────
    seen = set()
    unique: list[tuple[str, str]] = []
    for pair in expanded:
        if pair not in seen:
            seen.add(pair)
            unique.append(pair)

    print(f"After expanding to 3 styles: {len(expanded)} entries")
    print(f"After deduplication:         {len(unique)} entries")

    # ── 4. Write — alternating Khmer line then Latin line ─────────────────────
    lines = []
    for kh, lat in unique:
        lines.append(kh)
        lines.append(lat)

    (DATA_DIR / "khmer_names_training.txt").write_text("\n".join(lines), encoding="utf-8")
    print(f"khmer_names_training.txt → {len(lines)} lines written")
